int ft_strchr(char n, char *str)
{
    int c;

    c = 0;
    while(str[c] != '\0')
    {
        c++;
    }
    while(str[c] != n)
    {
        c--;
        if (c < 0)
        {
            return(0);
        }

    }
    return (c);
   

}
