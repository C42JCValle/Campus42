int ft_isalpha (int c)
{
    if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') || (c >= '0' && c <= '9'))
    {
        return (1);
    }
    return (0);
}


#include <stdio.h>
int main (void)
{
    int c;

    c = ft_isalpha('7');
    printf("\n\n%d\n", c);
    return (0);
}
