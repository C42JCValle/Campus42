int ft_tolower(int c)
{
    if (c >= 'A' && c <= 'Z')
    {
        c = c + 32;
    }
    return (c);
}

#include <stdio.h>
int main(void)
{
    char c;

    c = ft_tolower('U');
    printf("%s\n",&c);
    return (0);
}
